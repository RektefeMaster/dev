// Rastgele konum seçme fonksiyonu
export const getRandomLocation = () => {
  const randomIndex = Math.floor(Math.random() * MOCK_LOCATIONS.length);
  return MOCK_LOCATIONS[randomIndex];
};

// Belirli sayıda rastgele konum seçme fonksiyonu
export const getRandomLocations = (count: number) => {
  const shuffled = [...MOCK_LOCATIONS].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};
